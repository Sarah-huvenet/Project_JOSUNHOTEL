package board.qna;

public class BoardCommentDTO {
	private int rnum;
	private int idx;
	private int numQna;
	private String content;
	
	public int getRnum() {
		return rnum;
	}
	public void setRnum(int rnum) {
		this.rnum = rnum;
	}
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public int getNumQna() {
		return numQna;
	}
	public void setNumQna(int numQna) {
		this.numQna = numQna;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
}
